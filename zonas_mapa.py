def dist_2d(x1, z1, x2, z2):
    return math.sqrt((x1 - x2)**2 + (z1 - z2)**2)

def norm(val, vmin, vmax):
    return (val - vmin) / (vmax - vmin) if vmax != vmin else 0.0

def punto_en_poligono(x, y, poligono):
    n = len(poligono)
    dentro = False
    p1x, p1y = poligono[0]
    for i in range(1, n + 1):
        p2x, p2y = poligono[i % n]
        if y > min(p1y, p2y):
            if y <= max(p1y, p2y):
                if x <= max(p1x, p2x):
                    if p1y != p2y:
                        xints = (y - p1y) * (p2x - p1x) / (p2y - p1y) + p1x
                    if p1x == p2x or x <= xints:
                        dentro = not dentro
        p1x, p1y = p2x, p2y
    return dentro

def obtener_zona(x, y):
    for nombre_zona, poligono in ZONAS_MAPA.items():
        if punto_en_poligono(x, y, poligono):
            return nombre_zona
    def dist_seg(px, py, x1, y1, x2, y2):
        dx, dy = x2 - x1, y2 - y1
        if dx == 0 and dy == 0: return math.sqrt((px-x1)**2+(py-y1)**2)
        t = max(0, min(1, ((px-x1)*dx+(py-y1)*dy)/(dx*dx+dy*dy)))
        return math.sqrt((px-x1-t*dx)**2+(py-y1-t*dy)**2)
    best_z, best_d = "Jungla", float('inf')
    for nz, poly in ZONAS_MAPA.items():
        for i in range(len(poly)):
            p1x, p1y = poly[i]; p2x, p2y = poly[(i+1)%len(poly)]
            d = dist_seg(x, y, p1x, p1y, p2x, p2y)
            if d < best_d: best_d = d; best_z = nz
    return best_z
