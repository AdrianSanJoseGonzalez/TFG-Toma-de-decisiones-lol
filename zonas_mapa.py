# Categorías semánticas de zona
ZONA_CATEGORIA = {}
for z in ZONAS_MAPA:
    zl = z.lower()
    if "fuente" in zl: ZONA_CATEGORIA[z] = "BASE"
    elif "nexo" in zl or "nexired" in zl or "nexiblue" in zl: ZONA_CATEGORIA[z] = "BASE"
    elif "inibi" in zl: ZONA_CATEGORIA[z] = "BASE"
    elif "baron" in zl: ZONA_CATEGORIA[z] = "OBJECTIVE"
    elif "drake" in zl: ZONA_CATEGORIA[z] = "OBJECTIVE"
    elif "rio" in zl: ZONA_CATEGORIA[z] = "RIVER"
    elif "jungla" in zl or "jungal" in zl: ZONA_CATEGORIA[z] = "JUNGLE"
    elif "top" in zl or "bot" in zl or "mid" in zl: ZONA_CATEGORIA[z] = "LANE"
    else: ZONA_CATEGORIA[z] = "OTHER"
